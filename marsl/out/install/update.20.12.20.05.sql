CREATE TABLE IF NOT EXISTS `app` (
  `id` INT(11) NOT NULL AUTO_INCREMENT,
  `name` VARCHAR(100) NOT NULL,
  `key` CHAR(32) NOT NULL,
  `secret` CHAR(128) NOT NULL,
  PRIMARY KEY (`id`),
  UNIQUE INDEX `name_UNIQUE` (`name` ASC),
  UNIQUE INDEX `key_UNIQUE` (`key` ASC),
  UNIQUE INDEX `secret_UNIQUE` (`secret` ASC))
ENGINE = InnoDB;

CREATE TABLE IF NOT EXISTS `pushtoken` (
  `id` INT(11) NOT NULL AUTO_INCREMENT,
  `type` VARCHAR(7) NOT NULL,
  `pushtoken` VARCHAR(100) NULL DEFAULT NULL,
  `endpoint` LONGTEXT NULL DEFAULT NULL,
  `key` VARCHAR(100) NULL DEFAULT NULL,
  `auth` VARCHAR(100) NULL DEFAULT NULL,
  PRIMARY KEY (`id`))
ENGINE = InnoDB;
